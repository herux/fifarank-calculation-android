package com.herux.fifarankcalc;

public class CountryTeam {
	
	private String name;
	private int rankingNo;
	private float point;
	private String pos;
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the rankingNo
	 */
	public int getRankingNo() {
		return rankingNo;
	}
	/**
	 * @param rankingNo the rankingNo to set
	 */
	public void setRankingNo(int rankingNo) {
		this.rankingNo = rankingNo;
	}
	/**
	 * @return the point
	 */
	public float getPoint() {
		return point;
	}
	/**
	 * @param point the point to set
	 */
	public void setPoint(float point) {
		this.point = point;
	}
	/**
	 * @return the pos
	 */
	public String getPos() {
		return pos;
	}
	/**
	 * @param pos the pos to set
	 */
	public void setPos(String pos) {
		this.pos = pos;
	}
	
	

}
